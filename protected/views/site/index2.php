… es una unidad de la Facultad de Ciencias Jurídicas y Políticas de la Universidad Mayor de San Simón, encargada de las tareas que exigen el estudio científico de la realidad nacional e internacional en el ámbito de las carreras de Derecho y Ciencia Política.

El IIJP fue creado el 21 de marzo de 1958 en sesión extraordinaria del Honorable Consejo Universitario a solicitud del entonces Decano de la Facultad de Derecho, Ciencias Sociales y Políticas, Dr. Renato Crespo Paniagua, quien fundamentó esta creación en la necesidad desarrollar trabajos científicos con carácter serio y rigor metodológico y realizar la programación de exámenes de grado.

Tiene a su cargo la promoción, planificación, dirección, supervisión y evaluación de los programas y proyectos de investigación curricular y extracurricular, jurídica y politológica.
 La investigación curricular comprende a la realizada por estudiantes de pre-grado en el marco de las actividades académicas y modalidades de titulación de sus respectivos programas de formación. 
La investigación extracurricular la desarrollan docentes, investigadores y profesionales, en respuesta a requerimientos académicos y político institucionales de la UMSS o de las instituciones regionales y nacionales.

A través de Resolución Rectoral Nº 14 de 14 de febrero de 2011, se aprobó el organigrama funcional de la Facultad de Ciencias Jurídicas y Políticas y por ende del Instituto de Investigaciones; aspecto que responde y coadyuva a desarrollar de manera eficiente el creciente número de actividades de investigación curricular y extracurricular y fortalece el Instituto de Investigación en los aspectos de organización y aprovechamiento de los recursos humanos. 



